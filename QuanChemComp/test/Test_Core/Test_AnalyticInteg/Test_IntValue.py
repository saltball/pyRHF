# @numba.njit
def mS(n1, n2, AO1, AO2):
    matS = np.zeros((n1, n2))
    for i in range(n1):
        for j in range(n2):
            matS[i][j] = SxyzDefold(AO1["expon"][i], AO2["expon"][j], AO1["r"], AO2["r"], *AO1["l"], *AO2["l"], 0)
    return matS

# @numba.njit
def mC2(n1, n2, AO1, AO2):
    matC = np.zeros((n1, n2))
    for i in range(n1):
        for j in range(n2):
            matC[i][j] = AO1["norm"][i] * AO2["norm"][j] * AO1["coef"][i] * AO2["coef"][j]
    return matC

