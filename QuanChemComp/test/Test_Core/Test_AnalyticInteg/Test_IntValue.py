# @numba.njit
def m_nuc_attract(n1, n2, AO1, AO2, rC):
    matNucA = np.zeros((n1, n2))
    for i in range(n1):
        for j in range(n2):
            a = AO1["expon"][i]
            b = AO2["expon"][j]
            p = a + b
            A = np.array(AO1["r"])
            B = np.array(AO2["r"])
            C = np.array(rC)
            matNucA[i][j] = phi_2c1e(*AO1["l"], *AO2["l"], a, b, A,B,C)
    return matNucA

# @numba.njit
def mC4(n1, n2, n3, n4, AO1, AO2, AO3, AO4):
    matC = np.zeros((n1, n2, n3, n4))
    for i1 in numba.prange(n1):
        for i2 in numba.prange(n2):
            for i3 in numba.prange(n3):
                for i4 in numba.prange(n4):
                    matC[i1][i2][i3][i4] = AO1["norm"][i1] * AO2["norm"][i2] * AO3["norm"][i3] * AO4["norm"][i4] * AO1["coef"][i1] * \
                                           AO2["coef"][i2] * AO3["coef"][i3] * AO4["coef"][i4]
    return matC


